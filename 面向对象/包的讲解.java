

public class 包的讲解 {
    public static void main(String[] args) {
        /* 
        包: 就是文件夹, 用来管理各种不同功能的Java类, 方便后期代码维护
        包名的规则:
            公司的域名反写 + 包的作用, 需要全部英文小写, 见名知意
        全类名:
            包名 + 类名
        使用其他类的规则:
            1. 使用同一个包中的类时, 不需要导包
            2. 使用java.lang包中的类时, 不需要导包
            3. 其他情况都需要导包
            4. 如果同时使用两个包中的同类名, 需要用全类名
         */
    }
}
