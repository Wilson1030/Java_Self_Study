package 设计对象并使用;

public class 类和对象 {
    public static void main(String[] args) {
        /* 
        类(设计图): 是对象共同特征的描述
        对象: 是真实存在的具体东西
        在java中必须先设计类, 才能获得对象
        定义类:
            格式:
                public class 类名 {
                    成员变量 (代表属性, 一般是名词)
                    成员方法 (代表行为, 一般是动词)
                    构造器
                    代码块
                    内部类 
                }
            举例:
                public class Phone {
                    //成员变量
                    String brand;
                    double price;
                    //成员方法
                    public void call() {

                    }
                    public void playGame() {

                    }
                }
        得到类:
            格式:
                类名 对象名 = new 类名();
            举例:
                Phone p = new Phone();
        使用对象:
            访问属性: 对象名.成员变量
            访问行为: 对象名.方法名(...)
         */
    }
}
