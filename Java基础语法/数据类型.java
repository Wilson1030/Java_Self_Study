public class 数据类型{
    public static void main(String[] args) {
        //byte
        byte b = 10;
        System.out.println(b); //10
        
        //short
        short s = 20;
        System.out.println(s); //20
        
        //int
        int i = 30;
        System.out.println(i); //30
        
        //long
        //如果要定义long类型的变量
        //在整数值后面要加入L作为后缀
        //建议使用大写L
        long n = 9999999999L;
        System.out.println(n);

        //float
        //如果要定义float类型的变量
        //在小数值后面要加入F作为后缀
        //建议使用大写F
        float f = 10.1F;
        System.out.println(f);

        //double
        double d = 20.2;
        System.out.println(d);

        //char
        char c = '中';
        System.out.println(c);

        //boolean
        //true false
        boolean o = true;
        System.out.println(o);
    }
}