public class 算术运算符 {
    public static void main(String[] args){
        // +
        System.out.println(3 + 2);
        // -
        System.out.println(5 - 1);
        // *
        System.out.println(7 * 9);
        //有小数参与计算, 结果有可能不精确
        System.out.println(1.1 + 1.01);
        //除法
        System.out.println(10 / 2);
        System.out.println(10 / 3);
        //取模
        System.out.println(10 % 2);
        System.out.println(10 % 3);
    }
}